    <li>1/2 cup yellow moong dal (split yellow lentils)</li>
    <li>2 cups mixed vegetables (carrots, peas, beans, cauliflower, etc.), chopped</li>
    <li>1 onion, finely chopped</li>
    <li>2 tomatoes, finely chopped</li>
    <li>1 green chili, chopped (optional)</li>
