                    <td>Protein</td>
                    <td>Approx. 15 g per serving (1 cup)</td>
                </tr>
                <tr>
                    <td>Carbohydrates</td>
                    <td>Approx. 30 g per serving (1 cup)</td>
                </tr>
                <tr>
                    <td>Fat</td>
                    <td>Approx. 15 g per serving (1 cup)</td>
                </tr>
                <tr>
                    <td>Fiber</td>
                    <td>Approx. 8 g per serving (1 cup)</td>
