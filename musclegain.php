                <li>1 bell pepper (capsicum), cut into chunks</li>
                <li>1 onion, cut into chunks</li>
                <li>1 tomato, cut into chunks (optional)</li>
                <li>1/2 cup thick yogurt (hung curd)</li>
                <li>1 tablespoon ginger-garlic paste</li>
