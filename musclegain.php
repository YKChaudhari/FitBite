                <li>1 red bell pepper, diced</li>
                <li>1 small onion, diced</li>
                <li>2 cloves garlic, minced</li>
                <li>2 tablespoons olive oil</li>
                <li>1 teaspoon smoked paprika</li>
                <li>1/2 teaspoon ground cumin</li>
