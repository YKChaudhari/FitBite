            <h2>Instructions:</h2>
            <ol>
                <li>Wash the dried kidney beans (rajma) thoroughly under running water.</li>
                <li>Soak the washed kidney beans in 4 cups of water for at least 6-8 hours or overnight.</li>
                <li>After soaking, drain the water and transfer the kidney beans to a pressure cooker.</li>
