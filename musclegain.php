                    <td>Approx. 2 g per serving (1 cup)</td>
                </tr>
                <tr>
                    <td>Sodium</td>
                    <td>Approx. 600 mg per serving (1 cup)</td>
                </tr>
            </table>

            <h2>Ingredients:</h2>
            <ul>
                <li>1 cup whole black lentils (urad dal)</li>
                <li>1/4 cup red kidney beans (rajma)</li>
                <li>4 cups water (for soaking)</li>
                <li>4 cups water (for cooking)</li>
                <li>2 tablespoons ghee or butter</li>
                <li>1 large onion, finely chopped</li>
                <li>2 tomatoes, pureed</li>
                <li>1 tablespoon ginger-garlic paste</li>
                <li>1 green chili, slit (optional)</li>
                <li>1/2 teaspoon cumin seeds</li>
                <li>1/2 teaspoon turmeric powder</li>
                <li>1 teaspoon red chili powder</li>
                <li>1 teaspoon garam masala</li>
                <li>1/2 cup cream</li>
                <li>Salt to taste</li>
                <li>Fresh cilantro leaves, chopped (for garnish)</li>
            </ul>

            <h2>Instructions:</h2>
            <ol>
                <li>Wash the whole black lentils and red kidney beans thoroughly under running water.</li>
                <li>Soak the washed lentils and kidney beans in 4 cups of water for at least 4-6 hours or overnight.</li>
                <li>After soaking, drain the water and transfer the lentils and kidney beans to a pressure cooker.</li>
                <li>Add 4 cups of fresh water to the pressure cooker and pressure cook for about 15-20 minutes until the lentils and kidney beans are soft and well-cooked.</li>
                <li>Once cooked, mash the lentils and kidney beans lightly using the back of a spoon or a potato masher. Set aside.</li>
                <li>In a separate pan, heat ghee or butter over medium heat.</li>
                <li>Add cumin seeds and let them splutter.</li>
                <li>Add chopped onions and sauté until they turn golden brown.</li>
                <li>Add ginger-garlic paste and slit green chili (if using). Sauté for another 2 minutes.</li>
                <li>Now, add pureed tomatoes and cook until the oil starts to separate from the masala.</li>
                <li>Add turmeric powder, red chili powder, and garam masala. Mix well.</li>
                <li>Add the cooked lentils and kidney beans to the pan. Mix well with the masala.</li>
                <li>Add salt to taste and adjust the consistency of the dal by adding water if necessary.</li>
                <li>Simmer the dal for about 15-20 minutes on low heat, stirring occasionally, to allow the flavors to blend.</li>
                <li>Once the dal reaches your desired consistency, add cream and mix well. Let it simmer for another 2-3 minutes.</li>
                <li>Garnish with freshly chopped cilantro leaves.</li>
                <li>Serve hot with rice, naan, or roti. Enjoy your delicious Daal Makhni!</li>
            </ol>

