                <h2> <b> <i> Daal Makhni </b> </i> </h2><br>
                <h3>Benificial for Muscle-gain</h3><br>
            <table>
                <tr>
                    <th>Category</th>
                    <th>Veg</th>
                </tr>
                <tr>
                    <th>Nutrient</th>
                    <th>Amount</th>
