<h2>Ingredients:</h2>
<ul>
    <li>2 cups basmati rice, washed and soaked for 30 minutes</li>
    <li>500 grams chicken, cut into pieces</li>
    <li>2 onions, thinly sliced</li>
    <li>2 tomatoes, chopped</li>
    <li>1/2 cup yogurt</li>
    <li>2 tablespoons ginger-garlic paste</li>
    <li>1/4 cup chopped mint leaves</li>
    <li>1/4 cup chopped coriander leaves</li>
    <li>2 green chilies, slit</li>
    <li>1 teaspoon cumin seeds</li>
    <li>1 cinnamon stick</li>
    <li>4-5 cloves</li>
    <li>4-5 green cardamom pods</li>
    <li>1 bay leaf</li>
    <li>1/2 teaspoon turmeric powder</li>
    <li>1 teaspoon red chili powder</li>
    <li>1 teaspoon biryani masala powder</li>
    <li>1/4 cup fried onions (birista)</li>
    <li>1/4 cup ghee or oil</li>
    <li>Salt to taste</li>
</ul>

<h2>Instructions:</h2>
<ol>
    <li>In a large pot, bring water to a boil and add soaked rice along with a bay leaf, some salt, and whole spices (cumin seeds, cinnamon stick, cloves, and green cardamom pods). Cook until the rice is 70% done. Drain and set aside.</li>
    <li>In a mixing bowl, marinate the chicken pieces with yogurt, ginger-garlic paste, turmeric powder, red chili powder, biryani masala powder, chopped mint leaves, chopped coriander leaves, slit green chilies, and salt. Let it marinate for at least 30 minutes.</li>
    <li>Heat ghee or oil in a heavy-bottomed pan or pressure cooker. Add thinly sliced onions and fry until they turn golden brown. Remove half of the fried onions and set aside for garnishing.</li>
    <li>Add chopped tomatoes to the remaining onions in the pan and cook until they turn soft and mushy.</li>
    <li>Add marinated chicken pieces to the pan and cook until they are partially cooked.</li>
    <li>Layer the partially cooked rice over the chicken in the pan.</li>
    <li>Sprinkle fried onions (birista) over the rice layer.</li>
    <li>Cover the pan tightly with a lid and cook on low heat for 15-20 minutes or until the chicken is fully cooked and the rice is done.</li>
    <li>Once done, gently fluff up the biryani with a fork.</li>
    <li>Garnish with the reserved fried onions.</li>
    <li>Serve hot with raita, salad, or your favorite side dish.</li>
    <li>Enjoy your flavorful and aromatic Chicken Biryani!</li>
</ol>
