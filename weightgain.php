<h2>Ingredients:</h2>
<ul>
    <li>1/2 cup rice</li>
    <li>1/2 cup split yellow lentils (moong dal)</li>
    <li>1 small onion, finely chopped</li>
    <li>1 small tomato, finely chopped</li>
    <li>1/2 inch piece of ginger, grated</li>
    <li>2 cloves garlic, minced</li>
    <li>1/4 teaspoon turmeric powder</li>
    <li>1/2 teaspoon cumin seeds</li>
    <li>1/2 teaspoon mustard seeds</li>
    <li>2-3 whole black peppercorns</li>
    <li>2 cups water</li>
    <li>1 tablespoon ghee or oil</li>
    <li>Salt to taste</li>
    <li>Fresh cilantro leaves for garnish</li>
</ul>

<h2>Instructions:</h2>
<ol>
    <li>Wash rice and lentils together until the water runs clear. Drain and set aside.</li>
    <li>Heat ghee or oil in a pressure cooker or deep pan.</li>
    <li>Add cumin seeds, mustard seeds, and whole black peppercorns. Let them splutter.</li>
    <li>Add chopped onions and sauté until they turn golden brown.</li>
    <li>Add grated ginger and minced garlic. Sauté for a minute.</li>
    <li>Add chopped tomatoes and cook until they turn soft and mushy.</li>
    <li>Add turmeric powder and salt. Mix well.</li>
    <li>Add washed rice and lentils. Stir to coat them with the masala.</li>
    <li>Add water and mix well.</li>
    <li>Cover the pressure cooker with its lid and cook for 3-4 whistles or until the khichdi is cooked and soft.</li>
    <li>If using a pan, cover and cook on low heat until the rice and lentils are fully cooked, stirring occasionally.</li>
