<h2>Ingredients:</h2>
<ul>
    <li>1 cup whole wheat flour (atta)</li>
    <li>Water, as needed</li>
    <li>Ghee (clarified butter), for cooking</li>
    <li>Salt, to taste</li>
</ul>

<h2>Instructions:</h2>
<ol>
    <li>In a mixing bowl, add whole wheat flour and a pinch of salt.</li>
    <li>Add water gradually and knead the flour into a smooth and pliable dough.</li>
    <li>Divide the dough into equal-sized balls.</li>
    <li>Take one dough ball and roll it out into a small circle using a rolling pin.</li>
    <li>Spread a little ghee over the rolled-out dough circle.</li>
    <li>Fold the circle into half to form a semi-circle.</li>
    <li>Spread a little more ghee over the semi-circle and fold it again to form a triangle.</li>
    <li>Roll out the triangle gently into a paratha of desired thickness.</li>
    <li>Heat a tawa or skillet over medium heat.</li>
    <li>Place the rolled-out paratha on the heated tawa and cook for about a minute.</li>
    <li>Flip the paratha and cook the other side for another minute.</li>
    <li>Spread a little ghee over the cooked side and flip again.</li>
    <li>Cook until both sides are golden brown and cooked through, pressing gently with a spatula.</li>
    <li>Remove the paratha from the tawa and repeat the process with the remaining dough balls.</li>
    <li>Serve hot with yogurt, pickle, or any desired accompaniment.</li>
    <li>Enjoy your delicious Whole Wheat Paratha with Ghee!</li>
</ol>
